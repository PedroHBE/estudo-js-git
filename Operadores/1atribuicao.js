// Atribuições aritiméticas "+; -; *; /; %"
let x = 10

// Quero atribuir +6 ao valor de "x" sem trocar o valor na variavel
x = 6 + x
console.log (x)

// Agora quer remover "-4" do valor de "x"
x = x - 4
console.log (x)

// Outra forma de realizar atribuição é colocar o sinal "aritimetico" antes do "="
let y = 10

y += 15 // y = y + 15
y -= 5  // y = y - 5
y *= 2 // y = y * 2
y /= 2 // y = y / 2
y %= 2 // y = y % 2
console.log (y)

// Atribuições de texto
let w = "texto"

w += " atribuido com mais informações"
